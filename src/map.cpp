Map::Map()
{
    tileset = MediaFiles::CreateTexture("resources/img/map_tileset.png");

    LoadMap(lvl1);

    src.y = 0;
    dest.h = dest.w = 42;
    src.h  = src.w  = 20;

    dest.x = dest.y = 0;
}

Map::~Map(){};

void Map::LoadMap(int arr[22][70])
{
    for(int row = 0; row< 22; ++row){
        for (int column = 0; column < 70; ++column)
        {
            map[row][column] = arr[row][column];
        }
    }
}

void Map::Draw()
{
    int type;

    for(int row = 0; row< 22; ++row){
        for (int column = 0; column < 70; ++column)
        {
            type = map[row][column];

            dest.x = column * 32;
            dest.y = row * 32;

            switch (type)
            {
            //Dirt left
            case 1:
                src.x = 48;
                SDL_RenderCopy(OverLord::renderer, tileset, &src, &dest);
                //MediaFiles::DrawMap(tileset, src, dest);
            break;

            //Dirt Center
            case 2:
                src.x = src.y = 0;
                SDL_RenderCopy(OverLord::renderer, tileset, &src, &dest);
                //MediaFiles::DrawMap(tileset, src, dest);
            break;

            //Dirt Right.
            case 3:
                src.x = 72;
                SDL_RenderCopy(OverLord::renderer, tileset, &src, &dest);
                //MediaFiles::DrawMap(tileset, src, dest);
            break;

            default:
                break;
            }
        }
    }
}